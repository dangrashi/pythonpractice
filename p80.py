# 1. Create a dictionary
student = {
    "name": "Alice",
    "age": 20,
    "course": "Computer Science"
}

# 2. Access a value using its key
print(student["name"])  # Output: Alice

# 3. Add or Update an item
student["grade"] = "A"  # Adds a new key-value pair
student["age"] = 21     # Updates the value for key 'age'

# 4. Remove an item
del student["course"] 